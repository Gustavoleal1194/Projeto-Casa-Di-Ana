import * as React from "react"
import { Slot } from "@radix-ui/react-slot"
import { cva, type VariantProps } from "class-variance-authority"

import { cn } from "@/lib/utils"

const buttonVariants = cva(
  "inline-flex items-center justify-center gap-2 whitespace-nowrap rounded-xl text-sm font-serif font-medium transition-all duration-200 disabled:pointer-events-none disabled:opacity-50 [&_svg]:pointer-events-none [&_svg:not([class*='size-'])]:size-4 shrink-0 [&_svg]:shrink-0 outline-none focus-visible:border-ring focus-visible:ring-ring/50 focus-visible:ring-[3px] aria-invalid:ring-destructive/20 dark:aria-invalid:ring-destructive/40 aria-invalid:border-destructive",
  {
    variants: {
      variant: {
        default: "bg-casa-secondary text-white hover:bg-casa-accent shadow-md hover:shadow-lg font-serif",
        destructive:
          "bg-destructive text-white hover:bg-destructive/90 focus-visible:ring-destructive/20 dark:focus-visible:ring-destructive/40 dark:bg-destructive/60",
        outline:
          "border-2 border-casa-secondary text-casa-secondary hover:bg-casa-secondary hover:text-white shadow-sm hover:shadow-md font-serif",
        secondary:
          "bg-casa-accent text-white hover:bg-casa-accent/90 shadow-md hover:shadow-lg font-serif",
        ghost:
          "hover:bg-casa-secondary/10 hover:text-casa-secondary text-casa-primary",
        link: "text-casa-secondary underline-offset-4 hover:underline hover:text-casa-accent",
        casa: "bg-casa-secondary text-white hover:bg-casa-accent shadow-md hover:shadow-lg font-serif",
        "casa-outline": "border-2 border-casa-secondary text-casa-secondary hover:bg-casa-secondary hover:text-white shadow-sm hover:shadow-md font-serif",
      },
      size: {
        default: "h-10 px-6 py-2 has-[>svg]:px-4",
        sm: "h-8 rounded-lg gap-1.5 px-4 has-[>svg]:px-3",
        lg: "h-12 rounded-xl px-8 has-[>svg]:px-6 text-base",
        icon: "size-10",
        "icon-sm": "size-8",
        "icon-lg": "size-12",
      },
    },
    defaultVariants: {
      variant: "casa",
      size: "default",
    },
  }
)

function Button({
  className,
  variant,
  size,
  asChild = false,
  ...props
}: React.ComponentProps<"button"> &
  VariantProps<typeof buttonVariants> & {
    asChild?: boolean
  }) {
  const Comp = asChild ? Slot : "button"

  return (
    <Comp
      data-slot="button"
      className={cn(buttonVariants({ variant, size, className }))}
      {...props}
    />
  )
}

export { Button, buttonVariants }
